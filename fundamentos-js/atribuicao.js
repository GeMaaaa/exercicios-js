const a = 7
let b = 3

b += a // b = b + a 'adição'
console.log(b)

b -= 4 // b = b - 4 'subtração'
console.log(b)

b *= 2 // b = b * 2 'multiplicação'
console.log(b)

b /= 2 // b = b / 2 'divisão'
console.log(b)

b %= 2 // b = b % 2 'se for um valor par, o resultado vai ser 0, se for impar, vai ser 1'
console.log(b)